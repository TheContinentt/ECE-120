; The table below represents an 8x16 font.  For each 8-bit extended ASCII
; character, the table uses 16 memory locations, each of which contains
; 8 bits (the high 8 bits, for your convenience) marking pixels in the
; line for that character.
; I use a bitmask x8000, and by anding it with value in that row. The program could determine whether the 1st bit is 1, then goes to different branches and print different characters. Also I load the value in x5002 first, then times it to 16 and plus 14 to reach the address of ASCII code. Then use iterations of columns and rows to print each character in sequence.
;R0 stores the value of x5002 and also used as a temporary register. But after each use as temporary register, I reset its value to x5002
;R1 as the line numbers for each row. after each row finishes, it increases by 1.
;R2 first is a reading loop counter, then it's a row counter.
;R3 is a temporary register for storing bitmask results and different address.
;R4 stores the value for each row. And by iteration, it shifts left for each bitmask comparation.
;R5 as a bitmask constant whose value is x8000
;R6 first is a bitmask counter, then I reset it and it becomes a column counter.

        .ORIG   x3000          ;set the original address as x3000
	AND R0, R0, #0	       ;clear R0
	LEA R0, LEA_DATA             
	LDR R0, R0, #0         ;load x5002 to R0 
	AND R1, R1, #0         ;clear R1
	AND R2, R2, #0         ;clear R2
	AND R3, R3, #0         ;clear R3
	AND R4, R4, #0         ;clear R4
	AND R6, R6, #0         ;clear R6
	LEA R3, FONT_DATA      ;set R3 to the address of FONT_DATA
	ADD R6, R6, #14	       ;set R6 to 16 as a column counter
	AND R5, R5, #0         ;clear R5
	ADD R5, R5, #1         ;set R5 to 1
	ADD R2, R2, #3         ;set R2 as a counter for the bitmask first
	LDR R1, R0 ,#0         ;load the value in address x5002 to R1
MASK_LOOP                      ;here we make R5 equal x8000
	ADD R5, R5, R5         ;add R5 to x8000 which is a bitmask constant
	ADD R6, R6, #-1        ;R6 as a counter
	BRzp MASK_LOOP
READING_LOOP                   ;in this loop we read line number of ascii code in x5002
	ADD R1, R1, R1         ;add R1 t0 16R1 as the number of lines
	ADD R2, R2, #-1        ;R2 as a counter	            
	BRzp READING_LOOP
	ADD R1, R1, #14        ;adjust line numbers
	ADD R1, R1, R3         ;set R1 as the address of first line to the value in x5002
	AND R2, R2, #0         ;clear R2 as a new register       
	ADD R2, R2, #15        
	ADD R2, R2, #1         ;add R2 as a row counter whose value is #16
COLUMN_LOOP                    ;here is the column loop to make it print 16 columns
	AND R6, R6, #0         ;reset R6
	ADD R6, R6, #8         ;set R6 as a column counter
	ADD R2, R2, #-1        ;decrease row counter
	BRn DONE
	ADD R1, R1, #1         ;change the address to next line
	LDR R4, R1, #0         ;load the value of next line to R4
ROW_LOOP                       ;here is the row loop to print 16 rows
	AND R3, R3, #0         ;clear R3
	AND R3, R4, R5         ;determine whether the first bit is 0 or 1
	ADD R6, R6, #-1        ;decrease column counter
	BRn NEXT_ROW           ;if R6 is negative, then it would turn to next row
	ADD R4, R4, R4         ;left shift the value of this line to print 2nd bit
	ADD R3, R3, #0
	BRnp OUTPUT_ONE
OUTPUT_ZERO                    ;here is the branch if the bitmask bit is 0
	AND R3, R3, #0         ;clear R3
	ADD R3, R0, #-2        ;load R3 as x5000
	LDR R0, R3, #0         ;load ascii code in x5000 to R0
	OUT                    ;print character in R0
	LEA R0, LEA_DATA       ;reset R0 to x5002
	LDR R0, R0, #0         ;reset R0 to x5002
	BRnzp ROW_LOOP         ;back to row loop
OUTPUT_ONE                     ;here is the branch if the bitmask bit is 1
	AND R3, R3, #0         ;clear R3
	ADD R3, R0, #-1        ;load R3 as x5001
	LDR R0, R3, #0         ;load value in x5001 to R0
	OUT                    ;print character in x5001
	LEA R0, LEA_DATA       ;reset R0 to x5002
	LDR R0, R0, #0         ;reset R0 to x5002
	BRnzp ROW_LOOP         ;back to row loop
NEXT_ROW                       ;here is the branch to end current row
	LD R0, ASCII_NL        ;load x0A to R0
	OUT                    ;print x0A to the next row
	LEA R0, LEA_DATA       ;reset R0 to x5002
	LDR R0, R0, #0         ;reset R0 to x5002
	BRnzp COLUMN_LOOP      ;back to column loop
DONE
	HALT                   ;stop the program
ASCII_NL                       ;here store the value x0A
	.FILL	x0A
